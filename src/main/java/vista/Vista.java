    private void jBCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBCalcularActionPerformed
        // TODO add your handling code here:
        double masaCorporal = Double.parseDouble(jTFMasa.getText());
        double estatura = Double.parseDouble(jTFEstatura.getText());
        double IMC = Calculadora.calcularIMC(masaCorporal, estatura);
        char genero = buttonGroup1.getSelection().getActionCommand().charAt(0);
        mostrarIMC("su IMC es: " + IMC);
        mostrarEstado(genero, IMC);
        mostrarPesoIdeal(genero, estatura);
    }//GEN-LAST:event_jBCalcularActionPerformed

    private void mostrarIMC (String msjIMC){
        jLIMC.setText(msjIMC); 
    }
    
    private void mostrarEstado (char genero, double IMC){
        String estado = Calculadora.calcularEstado(genero, IMC);
        jLEstado.setText("Tienes: " + estado);
    }
    
    private void mostrarPesoIdeal (char genero, double estatura){
        String pesoIdeal = Calculadora.calcularPesoIdeal(genero, estatura);
        jLRecomendacion.setText(pesoIdeal);
    }
    
